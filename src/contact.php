            formulario.addEventListener('submit', function (e) {
                e.preventDefault();
                var dadosDoFormulario = new FormData(formulario);
        
                // Substitua esta URL pelo endereço do servidor onde deseja enviar os dados
                var urlDoServidor = 'https://seuservidor.com/receber-formulario';
        
                fetch(urlDoServidor, {
                    method: 'POST',
                    body: dadosDoFormulario
                })
                .then(function(response) {
                    if (response.ok) {
                        return response.text();
                    }
                    throw new Error('Erro no envio do formulário.');
                })
                .then(function(data) {
                    mensagemAgradecimento.innerHTML = 'Obrigado por nos contatar, a equipe irá ler e responder assim que possível.';
